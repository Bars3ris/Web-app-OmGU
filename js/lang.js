document
  .querySelectorAll('.lang button')
  .forEach((b) => b.addEventListener('click', setLang));

function setLang(){
  for (let key in langArr) {
    let elem = document.querySelector('.lng-' + key);
    if (elem) {
      elem.innerHTML = langArr[key][this.value];
    }
  }
}